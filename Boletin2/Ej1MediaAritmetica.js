/*1. Crear  una  función  que  calcule  la  media  aritmética  
de  cinco  valores  que  se  le  pasan como argumentos.*/

function mediaAritmetica (arg1,arg2,arg3,arg4,arg5){
    let media = (arg1 + arg2 + arg3 + arg4 + arg5)/5;    
    console.log(`La media aritmética es: ${media}`)
}

mediaAritmetica(3,4,6,5,5);
mediaAritmetica(5,5,5,5,5);



